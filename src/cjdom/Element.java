package cjdom;

/**
 * This class is a wrapper for Web API Node (https://developer.mozilla.org/en-US/docs/Web/API/Element).
 */
public class Element extends Node {

    /**
     * Constructor.
     */
    public Element()
    {
        super();
    }

    /**
     * Constructor.
     */
    public Element(Object jsObj)
    {
        super(jsObj);
    }
}
